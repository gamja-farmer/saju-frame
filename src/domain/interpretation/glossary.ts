/** locale에 맞는 용어 설명 반환 */
export function getGlossaryDescription(term: string, locale: GlossaryLocale): string | undefined {
  if (locale === 'ko') {
    return GLOSSARY_KO[term] ?? GLOSSARY[term];
  }
  return GLOSSARY[term];
}

